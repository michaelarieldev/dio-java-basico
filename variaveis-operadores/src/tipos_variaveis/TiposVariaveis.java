package tipos_variaveis;

public class TiposVariaveis {

	public static void main(String[] args) {

		// Os oito tipos primitivos
		// byte, short, int, long, float, double, boolean e char

		byte idade = 127;
		short ano = 2024;
		int cep = 21070333;
		long cpf = 9876543210L; // se começar com zero, usar outro tipo, exempli string
		float pi = 3.14F; // se começar com zero, usar outro tipo, exempli string
		double salario = 3450.19;

		// Constante
		// O valor não pode mudar

		final double VALOR_DE_PI = 3.14;
	}

}
